"""
Comments can be used to:

1. Explain Python code
2. Make the code more readable
3. To prevent execution when testing code
"""

# Single line comment with # symbol:

# Example:
# print("Hello, World!")

# Example:
print("Hello, World!") # This is a comment


# Multiple line comment:

# Example:
# This is a comment
# This is the second line
# More than just one line
print("Hello, World!")

# Example:
"""
This is a comment
This is the second line
More than just one line
"""
print("Hello, World!")