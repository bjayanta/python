
# if statement
mark = 33
if mark >= 33:
    print("Pass")

print("Program end.")

# if ... else statement
mark = 20
if mark >= 33:
    print("Pass")
else:
    print("Fail")
print("Program end.")

# if, elif statement
mark = 75
if mark >= 80:
    print("A+")
elif mark >= 70:
    print("A")
elif mark >= 60:
    print("A-")
elif mark >= 50:
    print("B")
elif mark >= 40:
    print("D")
elif mark >= 33:
    print("C")
else:
    print("F")
    
