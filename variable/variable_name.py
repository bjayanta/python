"""
Variable Name:

Short name (like x and y) or a more descriptive name (age, carname, total_volume). 
Rules for Python variables:

1. Must start with a letter or the underscore character
2. Cannot start with a number
3. Only contain alpha-numeric characters and underscores (A-z, 0-9, and _ )
4. Variable names are case-sensitive (age, Age and AGE are three different variables)
5. Cannot be any of the Python keywords
"""

# Legal variable names:
myname = "Jayanta"
my_name = "Jayanta"
_my_name = "Jayanta"
myName = "Jayanta"
MYNAME = "Jayanta"
myname2 = "Jayanta"

# Illegal variable names:
# 2myvar = "Jayanta"
# my-var = "Jayanta"
# my var = "Jayanta"

# Multi Words Variable Names
"""
Variable names with more than one word can be difficult to read.
There are several techniques you can use to make them more readable:
"""

# Camel Case
myVariableName = "Jayanta"

# Pascal Case
MyVariableName = "Jayanta"

# Snake Case
my_variable_name = "Jayanta"
